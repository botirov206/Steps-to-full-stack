// Arithmetic Operations

// Addition: 10 + 5 = 15
console.log(10 + 5)  // 15

// Subtraction: 10 - 5 = 5
console.log(10 - 5)  // 5

// Multiplication: 10 * 5 = 50
console.log(10 * 5)  // 50

// Division: 10 / 5 = 2
console.log(10 / 5)  // 2

// Modulus (remainder): 10 % 3 = 1
console.log(10 % 3)  // 1

// Exponentiation: 2 ** 3 = 8
console.log(2 ** 3)  // 8



